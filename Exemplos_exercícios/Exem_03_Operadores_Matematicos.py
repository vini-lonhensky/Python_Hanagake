#inicio
#Variaveis
#substituir para entrada do usúario (input)
numeroUm = 10
numeroDois = 5
resultado = 0 #variavel para receber o resultado da operação
#Soma
resultado = numeroUm + numeroDois
print(numeroUm, ' + ', numeroDois, ' = ',resultado)#15
#Subtração
resultado = numeroUm - numeroDois
print(numeroUm, ' - ', numeroDois, ' = ',resultado)#5
#Multiplicação
resultado = numeroUm * numeroDois
print(numeroUm, ' * ', numeroDois, ' = ', resultado)#50
#Divisão
resultado = numeroUm / numeroDois
print(numeroUm, ' / ', numeroDois, ' = ', resultado)#2.0
